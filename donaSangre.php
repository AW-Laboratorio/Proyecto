				<h1> Dona sangre </h1>
				<h3> “Vengas a lo que vengas, ven a donar”, la nueva campaña madrileña para aumentar las donaciones de sangre </h3>
				<p>EUROPA PRESS.-  La Comunidad de Madrid ha comenzado en los hospitales de la región una campaña de fomento de la donación de sangre, en la que a través de diversas señales se indicará a los visitantes, familiares y acompañantes de los pacientes cómo llegar a los puntos de donación de cada centro. El consejero de Sanidad, Jesús Sánchez Martos, ha presentado este lunes en el Hospital General Universitario Gregorio Marañón la campaña ‘Vengas a lo que vengas, ven a donar’, en la que ha explicado esta iniciativa del Centro de Transfusión de la Comunidad.</p>
				<p>Sánchez Martos ha invitado a seguir “los indicadores en las puertas y en los suelos” que habrá a partir de ahora en los hospitales para mantener “las novecientas donaciones diarias” que se necesitan en la Comunidad. “Cada donación de sangre salva tres vidas”, ha recordado el consejero de Sanidad, que ha invitado a donar a todos los mayores de dieciocho años que pesen más de cincuenta kilos. Asimismo, Sánchez Martos ha resaltado que la donación sanguínea es “el único medicamento genérico” tras el que no existe “ningún interés general”.</p>
				<p>El Centro de Transfusión coordina esta acción en 22 hospitales de la red pública madrileña, que instalarán vinilos en las puertas de entrada y en el suelo, soportes verticales en lugares de paso, pósteres verticales, carteles y folletos para indicar la ubicación de las salas de donación. Según datos del Centro de Transfusión de la Comunidad, el cuarenta por ciento de los nuevos donantes contestó en una encuesta que no había donado en el pasado “porque no se lo había planteado”. Por ello, según informa, el objetivo principal de esta campaña es que todas las personas se lo planteen y, con ello, aumente el número de donaciones.</p>
				<p>Se estima que en torno a 80.000 madrileños al año requieren transfusiones en distintas intervenciones, terapias o tratamientos para salvar sus vidas. En 2016 se llevaron a cabo 128.315 donaciones de sangre en los hospitales madrileños, lo que representa un 51 por ciento del total en la Comunidad de Madrid. La Comunidad cuenta con 32 puntos de donación y todos los hospitales ofrecen tres horas gratis de aparcamiento a los donantes. Se suman a ellos las veinte colectas diarias de las unidades móviles de la Comunidad y de Cruz Roja y el propio Centro de Transfusión.</p>
